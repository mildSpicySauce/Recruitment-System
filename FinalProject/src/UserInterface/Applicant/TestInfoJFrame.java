    TestInfoJFrame(ApplyRequest applyRequest) {
        this.applyRequest = applyRequest;
        this.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
        initComponents();
        applicantTfl.setText(applyRequest.getAccount().getApplicant().getFullName());
        if (applyRequest.getExamRequest() != null) {
            examEnterpriseTfl.setText(applyRequest.getExamRequest().getEnterprise().getName());
            
            testStatusTfl.setText(applyRequest.getExamRequest().getExamStatus().toString());
            if(applyRequest.getExamRequest().getExamStatus()== ExamStatus.Finished){
                scoreTfl.setText(Integer.toString(applyRequest.getExamRequest().getScore()));
            }else{
                scoreTfl.setText("not available");
            }
        }else{
            examEnterpriseTfl.setText("not ready");
            testStatusTfl.setText("Test hasn't been created");
            scoreTfl.setText("not available");
        }
        if(applyRequest.getExamRequest()!= null && applyRequest.getExamRequest().getExamStatus().equals(ExamStatus.Ready)){
            acceptBtn.setEnabled(true);
        }else{
            acceptBtn.setEnabled(false);
        }

    }

