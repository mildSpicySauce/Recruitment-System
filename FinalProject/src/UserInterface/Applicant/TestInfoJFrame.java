    private void acceptBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_acceptBtnActionPerformed
        if (applyRequest.getExamRequest().getExamStatus() == ExamStatus.Ready) {
            applyRequest.getExamRequest().setExamStatus(ExamStatus.ExamAccepted);
            JOptionPane.showMessageDialog(null, "Accept test successfully!");
            testStatusTfl.setText(ExamStatus.ExamAccepted.toString());
            acceptBtn.setEnabled(false);
        } else {
            JOptionPane.showMessageDialog(null, "You have already accepted this test, cannot accept it twice!");
        }
    }//GEN-LAST:event_acceptBtnActionPerformed

