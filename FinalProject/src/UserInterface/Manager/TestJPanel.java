    public void populateExamTable () {
        DefaultTableModel dtm = (DefaultTableModel) examTable.getModel();
        dtm.setRowCount(0);
        
        for (WorkRequest request : enterprise.getWorkQ().getWorkRequests()) {
            if (request instanceof ExamRequest) {
                ExamRequest er = (ExamRequest) request;
                Object[] row = new Object[4];
                row[0] = er;
                row[1] = er.getAccount().getApplicant();
                row[2] = er.getExamStatus();
                dtm.addRow(row);
            }
        }
    }

