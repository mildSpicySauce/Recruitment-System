    private void editBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editBtnActionPerformed
        // TODO add your handling code here:
        int index = examTable.getSelectedRow();
        if (index >= 0) {
    
            ExamRequest selectedRequest = (ExamRequest) examTable.getValueAt (index, 0);
            if(selectedRequest.getExamStatus().equals(ExamStatus.ExamAccepted)){
                EditTestResultJFrame editTestResultJFrame = new EditTestResultJFrame(system, selectedRequest.getAccount().getApplicant(), selectedRequest, this);
                editTestResultJFrame.setLocationRelativeTo(null);
                editTestResultJFrame.setVisible(true);
            }else if(selectedRequest.getExamStatus().equals(ExamStatus.Ready)){
                JOptionPane.showMessageDialog(null, "Applicants should accept the test first!");
            }else{
                JOptionPane.showMessageDialog(null, "Cannot edit test now!");
            }
                                
            
        }else{
            JOptionPane.showMessageDialog(null, "Please select a row!");
        }
    }//GEN-LAST:event_editBtnActionPerformed


